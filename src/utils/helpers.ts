import { values } from 'mobx';
import { store } from '../models/counters';

export const getAddress = (store: store, id: string) => {
  const address = values(store.addresses).find((address) => address.id === id);
  if (address) {
    return (
      address.house.address
        .replace('г Санкт-Петербург,', '')
        .replace('д ', 'д. ') +
      ', кв. ' +
      address.number
    );
  }
};

